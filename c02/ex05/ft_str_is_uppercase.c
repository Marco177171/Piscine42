int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	x;

	i = 0;
	x = 0;
	if (str[0] == '\0')
	{
		x = 1;
		return (x);
	}
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			x = 1;
		}
		else
		{
			return (0);
		}
		i++;
	}
	return (x);
}
