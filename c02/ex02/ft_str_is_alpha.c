int	ft_str_is_alpha(char *str)
{
	char	s;
	int		i;
	int		x;

	i = 0;
	x = 1;
	while (str[i] != '\0')
	{
		s = str[i];
		if (!((s >= 'a' && s <= 'z') || (s >= 'A' && s <= 'Z')))
		{
			x = 0;
			break ;
		}
		i++;
	}
	return (x);
}
